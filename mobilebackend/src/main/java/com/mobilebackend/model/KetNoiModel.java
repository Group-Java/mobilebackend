package com.mobilebackend.model;

public class KetNoiModel {
	private int maketnoi;
	private String wlan;
	private String buletooth;
	private String gps;
	public int getMaketnoi() {
		return maketnoi;
	}
	public void setMaketnoi(int maketnoi) {
		this.maketnoi = maketnoi;
	}
	public String getWlan() {
		return wlan;
	}
	public void setWlan(String wlan) {
		this.wlan = wlan;
	}
	public String getBuletooth() {
		return buletooth;
	}
	public void setBuletooth(String buletooth) {
		this.buletooth = buletooth;
	}
	public String getGps() {
		return gps;
	}
	public void setGps(String gps) {
		this.gps = gps;
	}
	

	
	
}
